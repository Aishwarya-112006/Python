# Truth table for OR , AND and NOT
# OR
print("Truth or Truth is",True or True) 
print("Truth or False is",True or False)
print("False or Truth is",False or True)
print("False or False is",False or False)  
# AND 
print("Truth and Truth is",True and True)
print("Truth and False is",True and False)
print("False and Truth is",False and True)  
print("False and False is",False and False)  
# NOT
print(not(True))
print(not(False))
